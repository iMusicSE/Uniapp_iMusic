				this.isPlaying = true;
				uni.hideLoading();
			});
			
			// 监听暂停事件
			this.innerAudioContext.onPause(() => {
				console.log('暂停播放');
				this.isPlaying = false;
			});
			
			// 监听停止事件
			this.innerAudioContext.onStop(() => {
				console.log('停止播放');
				this.isPlaying = false;
			});
			
			// 监听错误事件
			this.innerAudioContext.onError((error) => {
				console.error('播放错误:', error);
