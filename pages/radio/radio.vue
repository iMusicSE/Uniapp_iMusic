			if (this.currentStation && this.currentStation.id === station.id) {
				this.togglePlay();
				return;
			}
			
			// 停止当前播放
			if (this.innerAudioContext) {
				this.innerAudioContext.stop();
			}
			
			// 设置新电台
			this.currentStation = station;
			this.innerAudioContext.src = station.url;
			
			// 显示加载提示
			uni.showLoading({
				title: '连接中...',
				mask: true
			});
			
			// 开始播放
			try {
				this.innerAudioContext.play();
			} catch (error) {
				console.error('播放异常:', error);
				uni.hideLoading();
				uni.showToast({
					title: '播放失败',
					icon: 'none'
				});
			}
			
			// 5秒后自动隐藏加载提示
			setTimeout(() => {
				uni.hideLoading();
			}, 5000);
		},
		
