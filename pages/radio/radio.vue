		togglePlay() {
			if (!this.currentStation) return;
			
			if (this.isPlaying) {
				this.innerAudioContext.pause();
			} else {
				uni.showLoading({
					title: '连接中...',
					mask: true
				});
				this.innerAudioContext.play();
				
				// 3秒后自动隐藏加载提示
				setTimeout(() => {
					uni.hideLoading();
				}, 3000);
			}
		},
		
		// 停止播放
		stopPlay() {
			if (this.innerAudioContext) {
				this.innerAudioContext.stop();
			}
			this.currentStation = null;
			this.isPlaying = false;
		}
